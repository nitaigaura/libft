#include "libft.h"

// Sets a block of memory to a specific value.
void *ft_memset(void *b, int c, size_t len)
{
    size_t i;
    unsigned char *p = (unsigned char *)b;

    i = 0;
    while (i < len)
    {
        p[i] = (unsigned char)c;
        i++;
    }
    return (b);
}